def show_classification_stats(words):
    """Print classification statistics"""
    counts = Counter(classify_word(w) for w in words)
    sp("")
    sp("Classification stats (%d words):" % len(words))
    for cat, n in counts.most_common():
        sp("  %-10s: %3d" % (cat, n))


