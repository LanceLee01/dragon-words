def classify_word(word):
    """Return category for any word - uses the expanded dictionaries above."""
    w = word.lower().strip()

    # Remove periods for checking a.m., p.m.
    clean = w.replace(".", "")

    if clean in ANIMAL_WORDS or w in ANIMAL_WORDS:
        return "animal"
    if clean in COLOR_WORDS or w in COLOR_WORDS:
        return "color"
    if clean in ACTION_WORDS or w in ACTION_WORDS:
        return "action"
    if clean in EMOTION_WORDS or w in EMOTION_WORDS:
        return "emotion"
    if clean in PERSON_WORDS or w in PERSON_WORDS:
        return "person"
    if clean in POSITION_WORDS or w in POSITION_WORDS:
        return "position"
    if clean in FOOD_WORDS or w in FOOD_WORDS:
        return "food"
    if clean in NATURE_WORDS or w in NATURE_WORDS:
        return "nature"
    if clean in OBJECT_WORDS or w in OBJECT_WORDS:
        return "object"
    if clean in SIZE_WORDS or w in SIZE_WORDS:
        return "object"
    if clean in TIME_WORDS or w in TIME_WORDS:
        return "abstract"
    if clean in SOCIAL_WORDS or w in SOCIAL_WORDS:
        return "abstract"

    return "abstract"


