# Word list parser for middle school format
# ---------------------------------------------------------------------------
def parse_middle_word_list(filepath):
    """Parse the middle-school word list from Markdown table format.

    Format: | word | part_of_speech | chinese_meaning |
    Skips header rows, separator rows, and non-table content.
    Strips parenthetical variants like 'a (an)' -> 'a'
    """
    words = []
    with open(filepath, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            # Match table row: | word | pos | meaning |
            m = re.match(r'\|\s*(.+?)\s*\|\s*.+?\s*\|\s*.+?\s*\|', line)
            if m:
                raw = m.group(1)
                # Skip if it looks like a header or separator
                if raw.startswith(":") or raw.startswith("-"):
                    continue
                # Strip parenthetical variants
                word = re.sub(r'\s*\(.*?\)\s*', '', raw).strip()
                if word and len(word) > 0 and not word.startswith("-"):
                    words.append(word.lower())
    sp("Read %d words from %s" % (len(words), filepath))
    return words


